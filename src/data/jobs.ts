// Enhanced search function
export const searchJobs = (query: string, limit = 10): string[] => {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase().trim();
  
  // Check if the query matches an abbreviation
  const fullTitleFromAbbr = jobAbbreviations[lowerQuery];
  if (fullTitleFromAbbr) {
    return [fullTitleFromAbbr];
  }
  
  // Check for partial abbreviation matches
  const abbreviationMatches = Object.entries(jobAbbreviations)
    .filter(([abbr, _]) => abbr.startsWith(lowerQuery))
    .map(([_, fullTitle]) => fullTitle);
  
  // Remove duplicates from abbreviation matches
  const uniqueAbbreviationMatches = [...new Set(abbreviationMatches)];
  
  // Search in the main jobs list
  const exactMatches = popularJobs.filter(job =>
    job.toLowerCase().startsWith(lowerQuery)
  );
  
  const partialMatches = popularJobs.filter(job =>
    !job.toLowerCase().startsWith(lowerQuery) &&
    job.toLowerCase().includes(lowerQuery)
  );
  
  // Combine results, prioritizing abbreviation matches, then exact matches, then partial
  const allMatches = [
    ...uniqueAbbreviationMatches,
    ...exactMatches.filter(j => !uniqueAbbreviationMatches.includes(j)),
    ...partialMatches.filter(j => !uniqueAbbreviationMatches.includes(j) && !exactMatches.includes(j))
  ];
  
  return allMatches.slice(0, limit);
};

